package io.github.newbugger.android.screenshot.util;


public abstract class Singleton<T> {

    private T mInstance;

    protected abstract T create();

    public final T get() {
        synchronized (this) {
            if (mInstance == null) {
                mInstance = create();
            }
            return mInstance;
        }
    }

    public final T clear() {
        synchronized (this) {
            mInstance = null;
            return null;  // fake return
        }
    }

}
